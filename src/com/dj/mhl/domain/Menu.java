package com.dj.mhl.domain;

/**
 * menu表的javabean类
 */
public class Menu {
    private Integer id;//菜谱编号，唯一
    private String name;//菜谱名称
    private String type;//菜谱种类
    private Double price;//菜谱价格

    public Menu() {//反射使用
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public Double getPrice() {
        return price;
    }

    public void setPrice(Double price) {
        this.price = price;
    }

    @Override
    public String toString() {
        return "Menu{" +
                "id=" + id +
                ", name='" + name + '\'' +
                ", type='" + type + '\'' +
                ", price=" + price +
                '}';
    }
}
